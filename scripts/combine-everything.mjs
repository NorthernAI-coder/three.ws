#!/usr/bin/env node
// Combine every real documentation file across the whole repo — docs/, specs/,
// contracts/, the root README/STRUCTURE/CHANGELOG, and every package/worker/SDK
// README — into a single navigable file (docs/EVERYTHING.md).
// One-time snapshot generator: originals are never modified. Re-run manually to refresh.
//   node scripts/combine-everything.mjs
import { readFileSync, writeFileSync, existsSync } from 'node:fs';
import { execSync } from 'node:child_process';
import { relative, sep } from 'node:path';

const ROOT = process.cwd();
const OUT = 'docs/EVERYTHING.md';

// Same private gate as scripts/combine-docs.mjs — these subtrees name the GCP
// project, service accounts, and env vars that gate routes, or describe live
// attacks. Never let them leak into a document meant to be shared.
const PRIVATE_DOCS = new Set(['internal', 'ops', 'security']);

// Root-level files, taken individually.
const ROOT_FILES = ['README.md', 'STRUCTURE.md', 'CHANGELOG.md'];

// Directories whose markdown is real documentation (feature docs, specs,
// contract references, package/worker/SDK READMEs) rather than internal
// planning notes, generated reports, or vendored third-party mirrors.
const DOC_DIRS = [
  'docs',
  'specs',
  'contracts',
  'packages',
  'workers',
  'sdk',
  'solana-agent-sdk',
  'agent-payments-sdk',
  'page-agent-sdk',
  'x402-payment-modal',
  'x402-modal-sdk',
  'walk-sdk',
  'tour-sdk',
  'avatar-sdk',
  'apps-sdk',
  'agent-protocol-sdk',
  'agent-ui-sdk',
  'mcp-server',
  'mcp-bridge',
  'character-studio',
  'chat-plugin',
  'solana-mobile',
  'integrations',
  'extensions',
  'marketplace',
  'examples',
  'multiplayer',
  'services',
  'deploy',
  'chat',
  'blog',
  'api',
  'src',
];

const EXCLUDE_NAMES = new Set(['ALL.md', 'EVERYTHING.md']);

const isPrivate = (p) => {
  const top = p.split(sep)[1]; // e.g. docs/internal/foo.md -> 'internal'
  return p.startsWith('docs' + sep) && PRIVATE_DOCS.has(top);
};

const findMd = (dir) =>
  execSync(
    `find ${dir} -name '*.md' -not -path '*/node_modules/*' -not -path '*/dist/*' -not -path '*/build/*' -not -path '*/coverage/*' -not -path '*/test-results/*' -not -path '*/snapshots/*'`,
    { cwd: ROOT },
  )
    .toString()
    .trim()
    .split('\n')
    .filter(Boolean)
    .map((p) => p.replace(/^\.\//, ''));

let files = [
  ...ROOT_FILES.filter((f) => existsSync(f)),
  ...DOC_DIRS.filter((d) => existsSync(d)).flatMap(findMd),
];

files = [...new Set(files)]
  .filter((p) => !EXCLUDE_NAMES.has(p.split(sep).pop()))
  .filter((p) => !isPrivate(p))
  .sort();

// Group by top-level directory ("root" for the three bare root files).
const groupOf = (p) => {
  const parts = p.split(sep);
  return parts.length > 1 ? parts[0] : '(root)';
};

const sections = files.map((path, i) => {
  const anchor = `sec-${String(i + 1).padStart(4, '0')}`;
  const body = readFileSync(path, 'utf8').replace(/\s+$/, '');
  return { path, anchor, body, group: groupOf(path) };
});

const groups = [...new Set(sections.map((s) => s.group))];
const tocLines = [];
for (const g of groups) {
  tocLines.push(`\n### ${g}\n`);
  for (const s of sections.filter((x) => x.group === g)) {
    tocLines.push(`- [${s.path}](#${s.anchor})`);
  }
}

const totalBytes = sections.reduce((n, s) => n + Buffer.byteLength(s.body), 0);
const stamp = new Date().toISOString().slice(0, 10);

const header = `<!-- GENERATED by scripts/combine-everything.mjs — do not edit by hand. Edit the source files and re-run. -->
# three.ws — Everything

> Single-file snapshot of every real documentation file in the repo: \`docs/\`, \`specs/\`, \`contracts/\`, the root README/STRUCTURE/CHANGELOG, and every package/worker/SDK README. Generated ${stamp} from **${sections.length} files** (~${Math.round(totalBytes / 1024)} KB).
> The individual source files remain the canonical, editable copies — this file is a read-only aggregate. It excludes internal-only subtrees (\`docs/internal\`, \`docs/ops\`, \`docs/security\`) and build/vendor noise (planning notes under \`agents/\`/\`prompts/\`, generated \`reports/\`, vendored mirrors under \`public/\`/\`dist-lib/\`).

## Table of contents
${tocLines.join('\n')}

---
`;

const bodyOut = sections
  .map(
    (s) =>
      `<a id="${s.anchor}"></a>\n\n> **Source:** \`${s.path}\` · [↑ table of contents](#table-of-contents)\n\n${s.body}`,
  )
  .join('\n\n---\n\n');

writeFileSync(`${ROOT}/${OUT}`, `${header}\n${bodyOut}\n`);
console.log(`Wrote ${OUT}: ${sections.length} files, ${Math.round(totalBytes / 1024)} KB combined.`);
