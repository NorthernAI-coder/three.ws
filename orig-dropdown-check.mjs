import { chromium } from 'playwright';
const browser = await chromium.launch();
const page = await browser.newPage({ viewport: { width: 1440, height: 900 } });
await page.goto('http://localhost:3000/marketplace', { waitUntil: 'networkidle' });
await page.waitForSelector('.home-nav .nav-root', { timeout: 5000 });
await page.waitForTimeout(700);
const build = await page.locator('.home-nav .nav-trigger:has-text("Build")').first();
await build.click();
await page.waitForTimeout(400);
const menu = await page.locator('.home-nav .nav-trigger[aria-expanded="true"] + .nav-menu').first();
const box = await menu.boundingBox();
console.log('Original menu box:', JSON.stringify(box));
await page.screenshot({ path: '/tmp/orig-dropdown.png', clip: { x: 0, y: 0, width: 800, height: 300 } });
await browser.close();
