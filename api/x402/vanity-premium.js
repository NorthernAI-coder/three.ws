// GET /api/x402/vanity-premium
//   • no ?address        → browse the premium inventory (available patterns +
//                          difficulty-scaled prices). Public, no payment.
//   • ?address=<base58>  → buy that pre-ground address via x402. On payment the
//                          server reserves it, settles, and delivers the sealed
//                          key ONCE, then destroys the ciphertext (delete-after-
//                          reveal). The address is never served again.
//
// This is the SELL-FROM-STOCK tier: 4–5+ char brandable addresses ground ahead of
// time on batch CPU (workers/vanity-grinder) and held encrypted in
// vanity_inventory. It is separate from — and leaves untouched — the live $0.05
// grind tier (/api/x402/vanity, the vanity_grinder MCP tool), which grinds a
// fresh ≤3-char keypair per request.
//
// CUSTODY HONESTY (required, non-negotiable copy): the platform generated these
// keys. Every listing and every delivery tells the buyer plainly to use a bought
// address as a token MINT or to sweep assets to a self-generated wallet — never
// as a high-value treasury. See CUSTODY_NOTICE below.
//
// SECURITY: single-use delivery is enforced server-side in
// api/_lib/vanity-inventory-store.js (atomic status-guarded reveal). The
// plaintext key is NEVER logged and NEVER written to the x402 idempotency cache
// (the stored copy strips it, exactly like /api/x402/vanity). Optional
// sealTo=<X25519 pubkey> delivers the key sealed (ECIES) so plaintext never even
// appears in the response body.

